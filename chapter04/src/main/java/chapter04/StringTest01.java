package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		
		// "hello"
		System.out.println("\"hello\"");
		
		/* 제어 문자 */
		// \t : tab
		// \r : carrige return
		// \n : new line
		// \b : beep beep 
		System.out.println("hello\tworld\n");
		
		char c = '\'';
		System.out.println(c);
		

	}

}
